#Programar a conversão de uma temperatura digitada pelo usuário em graus Celsius para Fahrenheit.
# Mostrar o resultado na tela.

#1,8 e somar 32.

grausCelsius = input("Escreva a temparatura atual em graus Celsius: ")

resultado = (float(grausCelsius)) * float(1.8) + float(32)

print("Temperatura em Fahrenheit: ", resultado)

